    private void BtnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAgregarActionPerformed
        if(verificarEstadoComponentes()){
            String numControl = "";
            switch (ComboCarrera.getSelectedItem().toString()) {
                case "CP":
                    numControl = "C"+CajaNumeroControl.getText();
                    break;
                case "IA":
                    numControl = "I"+CajaNumeroControl.getText();
                    break;
                case "IM":
                    numControl = "M"+CajaNumeroControl.getText();
                    break;
                case "ISC":
                    numControl = "S"+CajaNumeroControl.getText();
                    break;
                case "LA":
                    numControl = "A"+CajaNumeroControl.getText();
                    break;
            } 
            alumno = new Alumno(numControl, CajaNombres.getText(), CajaPrimerApellido.getText(), CajaSegundoApellido.getText(), (byte)(int)SpinnerEdad.getValue(), (byte)(int)SpinnerSemestre.getValue(), ComboCarrera.getSelectedItem().toString());
            
            Alumno al = ALUMNO_DAO.buscarAlumno(numControl);
            if(al == null)
                if(CajaNumeroControl.getText().length() == 8)
                    if(ALUMNO_DAO.agregarAlumno(alumno)){
                        JOptionPane.showMessageDialog(rootPane, "Alumno agregado correctamente.");
                        establecerComponentesDefault();
                    }
                    else
                        JOptionPane.showMessageDialog(rootPane, "Error al agregar el alumno.");
                else
                    JOptionPane.showMessageDialog(rootPane, "El formato de Numero de Control debe incluir 8 digitos.");
            else
                JOptionPane.showMessageDialog(rootPane, "El numero de control ingresado ya existe.");
        }
        else
            JOptionPane.showMessageDialog(rootPane, "Aun existen campos vacios.");
        actualizarTabla(MOSTRAR_TODOS_LOS_REGISTROS);
    }//GEN-LAST:event_BtnAgregarActionPerformed

