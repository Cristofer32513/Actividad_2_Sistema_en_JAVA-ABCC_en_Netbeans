    public void restablecerCompontes(JComponent...componentes){
        for(JComponent c : componentes){
            if(c instanceof JTextField)
                ((JTextField) c).setText("");
            if(c instanceof JComboBox)
                ((JComboBox<?>) c).setSelectedIndex(0);
            if(c instanceof JSpinner)
                ((JSpinner) c).setValue(0);
        }		
    }
    
    public void juzgarComponente(){
        if(RadioButtonNombres.isSelected())
            if(!CajaNombres.getText().equals(""))
                BtnBuscar.doClick();
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        else if(RadioButtonPrimerApellido.isSelected())
            if(!CajaPrimerApellido.getText().equals(""))
                BtnBuscar.doClick();
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        else if(RadioButtonSegundoApellido.isSelected())
            if(!CajaSegundoApellido.getText().equals(""))
                BtnBuscar.doClick();
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        else if(RadioButtonEdad.isSelected())
            if((int)SpinnerEdad.getValue() != 0)
                BtnBuscar.doClick();
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        else if(RadioButtonSemestre.isSelected())
            if((int)SpinnerSemestre.getValue() != 0)
                BtnBuscar.doClick();
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        else if(RadioButtonCarrera.isSelected())
            if(ComboCarrera.getSelectedIndex() != 0)
                BtnBuscar.doClick();
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
    }
    
