    private void BtnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnBuscarActionPerformed
        if(RadioButtonNombres.isSelected()){
            if(!CajaNombres.getText().equals(""))
                actualizarTabla("SELECT * FROM Alumnos WHERE Nombres LIKE '%" + CajaNombres.getText() + "%'");
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        }
        else if(RadioButtonPrimerApellido.isSelected()){
            if(!CajaPrimerApellido.getText().equals(""))
                actualizarTabla("SELECT * FROM Alumnos WHERE Primer_Ap LIKE '%" + CajaPrimerApellido.getText() + "%'");
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        }
        else if(RadioButtonSegundoApellido.isSelected()){
            if(!CajaSegundoApellido.getText().equals(""))
                actualizarTabla("SELECT * FROM Alumnos WHERE Segundo_Ap LIKE '%" + CajaSegundoApellido.getText() + "%'");
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        }
        else if(RadioButtonEdad.isSelected()){
            if((int)SpinnerEdad.getValue() != 0)
                actualizarTabla("SELECT * FROM Alumnos WHERE Edad = " + (byte)(int)SpinnerEdad.getValue());
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        }
        else if(RadioButtonSemestre.isSelected()){
            if((int)SpinnerSemestre.getValue() != 0)
                actualizarTabla("SELECT * FROM Alumnos WHERE Semestre = " + (byte)(int)SpinnerSemestre.getValue());
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        }
        else if(RadioButtonCarrera.isSelected()){
            if(ComboCarrera.getSelectedIndex() != 0)
                actualizarTabla("SELECT * FROM Alumnos WHERE Carrera = '" + ComboCarrera.getSelectedItem().toString() + "'");
            else
                JOptionPane.showMessageDialog(rootPane, "El campo esta vacio.");
        }
        if(Tabla.getRowCount() == 0){
            limpiarTabla();
            JOptionPane.showMessageDialog(rootPane, "No se encontro ningun registro.");
        }
    }//GEN-LAST:event_BtnBuscarActionPerformed

