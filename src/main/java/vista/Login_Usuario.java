    public boolean verificarEstadoComponentes(){
        boolean bandera=true;

        if(cajaUsuario.getText().equals("Usuario..."))
            bandera=false;
        pass = new String(cajaPass.getPassword());
        if(pass.equals(""))
            bandera=false;

        return bandera;
    }
    
